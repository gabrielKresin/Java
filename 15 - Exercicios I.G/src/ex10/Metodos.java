package ex10;

public class Metodos {

	public double soma(double n1,double n2,double n3,double n4,double n5,double n6) {
		double r1 = n1*0.01;
		double r2 = n2*0.05;
		double r3 = n3*0.10;
		double r4 = n4*0.25;
		double r5 = n5*0.50;
		double r6 = n6*1;
		return r1+r2+r3+r4+r5+r6;
	}
}
