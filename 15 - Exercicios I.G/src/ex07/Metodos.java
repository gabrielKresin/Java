package ex07;

public class Metodos {

	public String tabuada(double num) {
		String tabuada = "";
		tabuada += num+" x 0 = 0\n";
		tabuada += num+" x 1 = "+num+"\n";
		tabuada += num+" x 2 = "+(num*2)+"\n";
		tabuada += num+" x 3 = "+(num*3)+"\n";
		tabuada += num+" x 4 = "+(num*4)+"\n";
		tabuada += num+" x 5 = "+(num*5)+"\n";
		tabuada += num+" x 6 = "+(num*6)+"\n";
		tabuada += num+" x 7 = "+(num*7)+"\n";
		tabuada += num+" x 8 = "+(num*8)+"\n";
		tabuada += num+" x 9 = "+(num*9)+"\n";
		tabuada += num+" x 10 = "+(num*10)+"\n";
		return tabuada;
	}
}
